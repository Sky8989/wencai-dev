public class OrderNationTime {
    private int on;
    private long delay;
    private long period;

    public int getOn() {
        return on;
    }

    public void setOn(int on) {
        this.on = on;
    }

    public long getDelay() {
        return delay;
    }

    public void setDelay(long delay) {
        this.delay = delay;
    }

    public long getPeriod() {
        return period;
    }

    public void setPeriod(long period) {
        this.period = period;
    }
}
